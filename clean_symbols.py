import re

with open('index.html', 'r', encoding='utf-8') as f:
    content = f.read()

# Полная замена всех проблемных Unicode-символов
replacements = {
    # Тире и дефисы
    '—': '-', '–': '-', '―': '-', '−': '-',
    '‐': '-', '‑': '-', '‑': '-', '‑': '-', '‑': '-',
    
    # Кавычки
    '"': '"', '"': '"', '"': '"', '"': '"',
    '«': '"', '»': '"', '‹': "'", '›': "'",
    ''': "'", ''': "'", ''': "'", ''': "'",
    '`': "'", '´': "'",
    
    # Многоточие
    '…': '...',
    
    # Стрелки
    '→': '->', '←': '<-', '↑': '^', '↓': 'v',
    '⇒': '=>', '⇐': '<=', '⇔': '<=>',
    
    # Валюта
    '₽': 'руб.', '$': 'USD', '€': 'EUR', '£': 'GBP',
    
    # Пробелы (неразрывные и другие)
    ' ': ' ', ' ': ' ', ' ': ' ', ' ': ' ',
    ' ': ' ', ' ': ' ', ' ': ' ',
    
    # Другие символы
    '⁄': '/', '∙': '*', '•': '-', '‣': '-',
    '°': ' град.', '±': '+/-', '×': 'x', '÷': '/',
    '¼': '1/4', '½': '1/2', '¾': '3/4',
    
    # Невидимые символы
    '': '', '': '', '': '', '': '',
    '': '', '': '', '': '',
    
    # Лишние форматирования
    '‌': '', '': '', '': '',
}

for old, new in replacements.items():
    content = content.replace(old, new)

with open('index.html', 'w', encoding='utf-8') as f:
    f.write(content)

print("Готово! Все спецсимволы заменены.")
print(f"Размер файла: {len(content)} байт")
